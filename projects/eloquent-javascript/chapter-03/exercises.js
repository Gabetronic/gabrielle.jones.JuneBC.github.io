// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

  function countChars(string, target){
//Create an output variable to hold the count - count starts at 0, so assign the var to 0.
  let count = 0;
//Iterate through the string
   for(var i = 0; i < string.length; i++){
//If target found, add 1 count to the output variable
     if(string[i] === target){
       count += 1;
     }
   }
//Return output variable
  return count;
 }

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string){
//Return the number of capital "B"s in the string parameter:
  return countChars(string, "B");
}

